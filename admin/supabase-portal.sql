-- ===================== ticket_messages =====================
-- Clients see only non-internal messages on their own tickets, and can
-- only ever insert non-internal ones.

drop policy if exists "agents read messages" on ticket_messages;
drop policy if exists "agents insert messages" on ticket_messages;
drop policy if exists "agents update messages" on ticket_messages;
drop policy if exists "agents delete messages" on ticket_messages;
drop policy if exists "scoped read messages" on ticket_messages;
drop policy if exists "scoped insert messages" on ticket_messages;

create policy "scoped read messages" on ticket_messages for select to authenticated
  using (
    is_agent()
    or (
      internal = false
      and exists (
        select 1 from tickets t
        where t.id = ticket_messages.ticket_id
          and t.client_id = my_client_id()
      )
    )
  );

create policy "scoped insert messages" on ticket_messages for insert to authenticated
  with check (
    is_agent()
    or (
      internal = false
      and exists (
        select 1 from tickets t
        where t.id = ticket_messages.ticket_id
          and t.client_id = my_client_id()
      )
    )
  );

create policy "agents update messages" on ticket_messages for update to authenticated
  using (is_agent());
create policy "agents delete messages" on ticket_messages for delete to authenticated
  using (is_agent());

