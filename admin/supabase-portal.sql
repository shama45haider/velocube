-- Velocube - Client Portal setup + security re-scoping
-- Run ONCE in the velocube project: Dashboard -> SQL Editor -> New query.
-- Run this AFTER supabase-clean-setup.sql and supabase-v3.sql.
-- Safe to re-run.
--
-- WHY THIS MATTERS
-- Until now every policy was "to authenticated using (true)", which was fine
-- because only staff had logins. Once clients can sign in to the same project,
-- that would let any client read every other client's data. This script
-- rewrites every policy so that:
--   * agents (email listed in the agents table) keep full access
--   * a client sees ONLY rows belonging to their own account
--   * internal staff notes never reach a client

-- ===================== Link auth users to clients =====================

alter table clients add column if not exists auth_user_id uuid references auth.users (id);
create unique index if not exists clients_auth_user_id_key on clients (auth_user_id);

-- ===================== Helper functions =====================
-- security definer so they can read the agents/clients tables without
-- triggering the very policies that call them (avoids infinite recursion).

create or replace function is_agent()
  returns boolean
  language sql
  security definer
  stable
  set search_path = public
as $$
  select exists (
    select 1 from agents
    where lower(email) = lower(auth.jwt() ->> 'email')
      and active
  );
$$;

create or replace function my_client_id()
  returns bigint
  language sql
  security definer
  stable
  set search_path = public
as $$
  select id from clients where auth_user_id = auth.uid() limit 1;
$$;

grant execute on function is_agent() to authenticated;
grant execute on function my_client_id() to authenticated;

