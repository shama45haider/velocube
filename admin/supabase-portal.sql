-- ===================== Safe client-facing view =====================
-- RLS is row-level, not column-level, so the portal reads the client
-- record through this view, which simply does not expose internal notes.

create or replace view client_self as
  select id, account_number, business, contact, email, phone, plan, monthly,
         term, storage_used, storage_limit, client_since, renewal_date, status
  from clients
  where auth_user_id = auth.uid();

grant select on client_self to authenticated;

-- ===================== Rewritten policies: clients =====================

drop policy if exists "agents read clients" on clients;
drop policy if exists "agents write clients" on clients;
drop policy if exists "agents insert clients" on clients;
drop policy if exists "agents delete clients" on clients;
drop policy if exists "scoped read clients" on clients;
drop policy if exists "scoped update clients" on clients;

create policy "scoped read clients" on clients for select to authenticated
  using (is_agent() or auth_user_id = auth.uid());
create policy "scoped update clients" on clients for update to authenticated
  using (is_agent() or auth_user_id = auth.uid());
create policy "agents insert clients" on clients for insert to authenticated
  with check (is_agent());
create policy "agents delete clients" on clients for delete to authenticated
  using (is_agent());

